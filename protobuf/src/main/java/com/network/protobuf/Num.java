package com.network.protobuf;

public class Num {
	private int id;
	private int num;
	
	public Num(int id, int num) {
		super();
		this.id = id;
		this.num = num;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
}
